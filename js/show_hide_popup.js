function fn_change_image_jquery(){	
	$("#img_image").mouseover(function() {
		$("#img_image").attr('src',"img/foco_over.png");			
	});
	$("#img_image").mouseout(function() {
		$("#img_image").attr('src',"img/foco_roll.png");			
	});
	
	$("#img_repetir").mouseover(function() {
		$("#img_repetir").attr('src',"../img/btn_repetir_over.png");			
	});
	$("#img_repetir").mouseout(function() {
		$("#img_repetir").attr('src',"../img/repertir_roll.png");			
	});
}